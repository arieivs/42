int	ft_is_sort(int *tab, int length, int (*f)(int, int))
{
	int i;

	i = 0;
	while (i < length - 1 && f(tab[i], tab[i + 1]) == 0)
		i++;
	if (i < length - 1 && f(tab[i], tab[i + 1]) < 0)
	{
		while (i < length - 1)
		{
			if (f(tab[i], tab[i + 1]) > 0)
				return (0);
			i++;
		}
	}
	else if (i < length - 1 && f(tab[i], tab[i + 1]) > 0)
	{
		while (i < length - 1)
		{
			if (f(tab[i], tab[i + 1]) < 0)
				return (0);
			i++;
		}
	}
	return (1);
}
